import express from "express";
import bodyParser from "body-parser";
import 'dotenv/config';  //load sensitive environment variables from .env file

const app = express();
const port = 3000;
const masterKey = process.env.MASTER_KEY; //load the master key from .env file

app.use(bodyParser.urlencoded({ extended: true }));

//1. GET a random joke
app.get("/random", (req, res) => {   //path /random is where we'll GET a random joke
  const randomIndex = Math.floor(Math.random() * jokes.length);
  res.json(jokes[randomIndex]);  // convert our object into json so that our app.get can use it
});

//2. GET a specific joke
app.get("/jokes/:id", (req, res) => {  //endpoint for GETting a specific joke
  const id = parseInt(req.params.id);  //they'll put id in the request parameters, we just need it as an integer
  const foundJoke = jokes.find((joke) => joke.id === id);  //.find is a method that takes any array and returns...
                                                           // the array element varred in () when the => condition is true
                                                           // a for each loop would've been another way to do it
  res.json(foundJoke);
});

//3. GET a jokes by filtering on the joke type
app.get("/filter", (req, res) => {
  const type = req.query.type;  //need .type to be spelled/cased right, since our api docs want it to be exactly that
  const filteredActivities = jokes.filter((joke) => joke.jokeType === type); //filter method is like find but returns ALL matches
  res.json(filteredActivities);
});

//4. POST a new joke
app.post("/jokes", (req, res) => {
  const newJoke = {
    id: jokes.length + 1,     //the elements are 0-99 but the id labels within are 1-100 so length+1 is appropriate
    jokeText: req.body.text,  //bodyParser standard retrieval of body.text and body.type properties
    jokeType: req.body.type,  
  };
  jokes.push(newJoke);
  console.log(jokes.slice(-1)); //access the joke at the last index (the one we just added)
  res.json(newJoke);            //quick easy way to pack the new joke into json
});

//5. PUT a joke
app.put("/jokes/:id", (req, res) => {
  const id = parseInt(req.params.id);  //get ID from postman param input
  const replacementJoke = {
    id: id,                           //use the old ID with the new everything else (that's a PUT)
    jokeText: req.body.text,
    jokeType: req.body.type,
  };

  const searchIndex = jokes.findIndex((joke) => joke.id === id);  // findIndex returns the Index when condition is true
                                                                  // it's kind of like the jokes.find(joke) that returns the whole joke
                                                                  // can use === since we converted to int to match exactly

  jokes[searchIndex] = replacementJoke;
  // console.log(jokes);
  res.json(replacementJoke);
});

//6. PATCH a joke
app.patch("/jokes/:id", (req, res) => {
  const id = parseInt(req.params.id);
  const existingJoke = jokes.find((joke) => joke.id === id);
  const replacementJoke = {
    id: id,
    jokeText: req.body.text || existingJoke.jokeText,   // if left side exists use it "OR" use right side (we know exists)
    jokeType: req.body.type || existingJoke.jokeType,
  };
  const searchIndex = jokes.findIndex((joke) => joke.id === id);
  jokes[searchIndex] = replacementJoke;
  console.log(jokes[searchIndex]);
  res.json(replacementJoke);
});

//7. DELETE Specific joke
app.delete("/jokes/:id", (req, res) => {
  const id = parseInt(req.params.id);
  const searchIndex = jokes.findIndex((joke) => joke.id === id);
  if (searchIndex > -1) {
    jokes.splice(searchIndex, 1);
    res.sendStatus(200);            //we get to set the status, based on success
  } else {
    res
      .status(404)
      .json({ error: `Joke with id: ${id} not found. No jokes were deleted.` });
  }
});

//8. DELETE All jokes
app.delete("/all", (req, res) => {
  const userKey = req.query.key;
  if (userKey === masterKey) {
    jokes = [];
    res.sendStatus(200);
  } else {
    res
      .status(404)
      .json({ error: `You are not authorised to perform this action.` });
  }
});

app.listen(port, () => {
  console.log(`Successfully started server on port ${port}.`);
});

